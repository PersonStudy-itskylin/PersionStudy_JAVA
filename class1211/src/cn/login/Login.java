package cn.login;

public class Login {
	private String userno;
	private String pwd;
	
	
	public String getUserno() {
		return userno;
	}
	
	public void setUserno(String userno) {
		this.userno = userno;
	}
	
	public String getPwd() {
		return pwd;
	}
	
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
}
