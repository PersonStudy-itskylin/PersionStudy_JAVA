package cn.reg;

public class Reg {
	private String userno;
	private String pwd;
	/**
	 * @return userno
	 */
	public String getUserno() {
		return userno;
	}
	/**
	 * @param userno 要设置的 userno
	 */
	public void setUserno(String userno) {
		this.userno = userno;
	}
	/**
	 * @return pwd
	 */
	public String getPwd() {
		return pwd;
	}
	/**
	 * @param pwd 要设置的 pwd
	 */
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
}
